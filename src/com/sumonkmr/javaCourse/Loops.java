package com.sumonkmr.javaCourse;

public class Loops {
    public static void main(String[] args) {


        // -- For loop
        for (int i = 0; i <= 10; i++){
            System.out.println(i+" Hi This is Sumon kmr in for loop");
        } // --For Loop end


        // --Do while loop
        int j = 1;

        do {
            System.out.println("Hi This is Sumon kmr in do-while loop");
            break;

        }while (j == 10);{
            System.out.println("First time the code will run then stop for false conditions");

        } // --Do while loop end
    }
}
